export const clienteSchema = z.object({
  id: z.number(),
  nome: z.string(),
  email: z.string().optional(),
  telefone_fixo: z.string().optional(),
  cpf: z.string(),
  endereco: z.string().optional(),
  cidade: z.string().optional(),
  estado: z.string().optional(),
  cep: z.string().optional(),
  data_nascimento: z.string().optional(),
  status: z.string().optional(),
  data_cadastro: z.string().optional(),
  ultima_compra: z.string().optional(),
})

const getStatusBadge = (status: string) => {
  switch (status) {
    case "Ativo":
      return (
        <Badge variant="outline" className="text-muted-foreground px-1.5">
          <IconCircleCheckFilled className="fill-green-500 dark:fill-green-400" />
          {status}
        </Badge>
      )
    case "Inativo":
      return (
        <Badge variant="outline" className="text-muted-foreground px-1.5">
          <IconX className="text-red-500" />
          {status}
        </Badge>
      )
    default:
      return (
        <Badge variant="outline" className="text-muted-foreground px-1.5">
          {status}
        </Badge>
      )
  }
}

const getCategoriaColor = (categoria: string) => {
  switch (categoria) {
    case "Premium":
      return "bg-purple-100 text-purple-800"
    case "Gold":
      return "bg-yellow-100 text-yellow-800"
    case "Silver":
      return "bg-gray-100 text-gray-800"
    case "Bronze":
      return "bg-orange-100 text-orange-800"
    default:
      return "bg-blue-100 text-blue-800"
  }
}

const createColumns = (
  onEdit: (cliente: Cliente) => void,
  onStatusChange?: () => void
): ColumnDef<z.infer<typeof clienteSchema>>[] => [
  {
    id: "select",
    header: ({ table }) => (
      <div className="flex items-center justify-center">
        <Checkbox
          checked={
            table.getIsAllPageRowsSelected() ||
            (table.getIsSomePageRowsSelected() && "indeterminate")
          }
          onCheckedChange={(value) => table.toggleAllPageRowsSelected(!!value)}
          aria-label="Select all"
        />
      </div>
    ),
    cell: ({ row }) => (
      <div className="flex items-center justify-center">
        <Checkbox
          checked={row.getIsSelected()}
          onCheckedChange={(value) => row.toggleSelected(!!value)}
          aria-label="Select row"
        />
      </div>
    ),
    enableSorting: false,
    enableHiding: false,
  },
  {
    accessorKey: "nome",
    header: "Cliente",
    size: 250, // Largura fixa para a coluna
    cell: ({ row }) => {
      const cliente = row.original

      // Função para truncar nome mantendo identificação
      const truncateNome = (nome: string, maxLength: number = 30) => {
        if (nome.length <= maxLength) return nome

        // Pega as primeiras palavras até o limite
        const palavras = nome.split(' ')
        let nomeReduzido = palavras[0] // Sempre mantém o primeiro nome

        for (let i = 1; i < palavras.length; i++) {
          const proximoNome = nomeReduzido + ' ' + palavras[i]
          if (proximoNome.length > maxLength - 3) break // -3 para os "..."
          nomeReduzido = proximoNome
        }

        return nomeReduzido + '...'
      }

      const nomeExibido = truncateNome(cliente.nome)
      const nomeCompleto = cliente.nome

      return (
        <div className="min-w-0 max-w-[250px]"> {/* Largura máxima controlada */}
          <TooltipProvider>
            <Tooltip>
              <TooltipTrigger asChild>
                <div className="font-medium truncate cursor-help">
                  {nomeExibido}
                </div>
              </TooltipTrigger>
              <TooltipContent side="top" className="max-w-xs">
                <p className="font-medium">{nomeCompleto}</p>
                <p className="text-xs text-muted-foreground mt-1">
                  CPF/CNPJ: {cliente.cpf}
                </p>
              </TooltipContent>
            </Tooltip>
          </TooltipProvider>
          <div className="text-sm text-muted-foreground truncate">
            {cliente.cpf}
          </div>
        </div>
      )
    },
    enableHiding: false,
  },
  {
    accessorKey: "email",
    header: "Contato",
    cell: ({ row }) => {
      const cliente = row.original
      return (
        <div>
          <div className="flex items-center gap-1">
            <IconMail className="h-3 w-3" />
            <span className="text-sm">{cliente.email}</span>
          </div>
          <div className="flex items-center gap-1">
            <IconPhone className="h-3 w-3" />
            <span className="text-sm">{cliente.telefone}</span>
          </div>
        </div>
      )
    },
  },
  {
    accessorKey: "endereco",
    header: "Localização",
    cell: ({ row }) => {
      const cliente = row.original
      return (
        <div>
          <div className="font-medium">{cliente.cidade}/{cliente.estado}</div>
          <div className="text-sm text-muted-foreground">{cliente.cep}</div>
        </div>
      )
    },
  },

  {
    accessorKey: "status",
    header: "Status",
    cell: ({ row }) => getStatusBadge(row.getValue("status")),
  },
  {
    accessorKey: "ultimaCompra",
    header: "Última Compra",
    cell: ({ row }) => (
      <div className="text-sm">{row.getValue("ultimaCompra")}</div>
    ),
  },
  {
    id: "actions",
    cell: ({ row }) => (
      <ClientesTableActions
        cliente={row.original as Cliente}
        onEdit={onEdit}
        onStatusChange={onStatusChange}
      />
    ),
  },
]

interface ClientesDataTableProps {
  data: z.infer<typeof clienteSchema>[]
  onEdit: (cliente: Cliente) => void
  onStatusChange?: () => void
  actionButtons?: React.ReactNode
}

